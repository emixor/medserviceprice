/** Pick a localized name from a service object based on active language. */
export function localizedServiceName(
  svc: { nameRu?: string; nameKk?: string; nameEn?: string } | null | undefined,
  lang: Lang
): string {
  if (!svc) return "";
  if (lang === "kk") return svc.nameKk || svc.nameRu || svc.nameEn || "";
  if (lang === "en") return svc.nameEn || svc.nameRu || svc.nameKk || "";
  return svc.nameRu || svc.nameKk || svc.nameEn || "";
}

